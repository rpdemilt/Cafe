public class Order {

	private double price;
	private int time; //time variable, in minutes
	private int orderNumber; 
	
	
	public double price()
	{
		return price;
	}

	public void setPrice(double p)
	{
		price = p;
	}

	public int time()
	{
		return time;
	}

	public void setPrice(int t)
	{
		time = t;
	}

	public int orderNumber()
	{
		return orderNumber;
	}

	public void setOrderNumber(int o)
	{
		orderNumber = o;
	}

}